/*
* Return with true if valid configuration is found, and its kernel_name (optional), block_size,
* grid_size as well.
* Return with false if no valid kernel configuration can be selected for the specified
* ConvolutionContext
*/
static inline bool FindImplicitGemmGtcDynamicFwdKernel(const ConvolutionContext& ctx,
                                                       std::string* p_kernel_name,
                                                       int* p_block_size,
                                                       int* p_grid_size)
{
    auto tunables  = GetImplicitGemmGtcDynamicFwdXdlopsTunablesList();
    int n          = ctx.batch_sz;
    int c          = ctx.n_inputs;
    int k          = ctx.n_outputs;
    int ho         = ctx.out_height;
    int wo         = ctx.out_width;
    int stride_h   = ctx.out_height > 1 ? ctx.kernel_stride_h : 1;
    int stride_w   = ctx.out_width > 1 ? ctx.kernel_stride_w : 1;
    int dilation_h = ctx.kernel_size_h > 1 ? ctx.kernel_dilation_h : 1;
    int dilation_w = ctx.kernel_size_w > 1 ? ctx.kernel_dilation_w : 1;
    int pad_h      = ctx.pad_h;
    int pad_w      = ctx.pad_w;
    int y          = ctx.kernel_size_h;
    int x          = ctx.kernel_size_w;

    int gemm_m = k;
    int gemm_n = n * ho * wo;
    int gemm_k = c * y * x;

    for(auto cfg : tunables)
    {
        if(cfg.nxe == 0)
        {
            if((x != 1) || (y != 1) || (stride_h != 1) || (stride_w != 1) || (dilation_h != 1) ||
               (dilation_w != 1) || (pad_h != 0) || (pad_w != 0))
            {
                continue;
            }
        };
        if((gemm_n % cfg.gemm_n_per_block != 0) || (gemm_m % cfg.gemm_m_per_block != 0) ||
           (gemm_k % cfg.gemm_k_per_block != 0))
        {
            continue;
        };
        // Don't have to check, assuming the tunable itself is already valid
        if(cfg.gemm_n_per_block % cfg.nxb != 0)
        {
            continue;
        };

        if(n % (cfg.gemm_n_per_block / cfg.nxb) != 0)
        {
            continue;
        };

        if((ho * wo) % cfg.nxb != 0)
        {
            continue;
        };

        // tensor_b_thread_lengths[c1e] > 1 can only be used with x=y=1
        if(cfg.tensor_b_thread_lengths[1] > 1 && (x != 1 || y != 1))
        {
            continue;
        };

        if(p_kernel_name != nullptr)
            *p_kernel_name = cfg.GetKernelName();

        if(p_block_size != nullptr)
        {
            *p_block_size = cfg.GetBlockSize();
        };

        if(p_grid_size != nullptr)
        {
            *p_grid_size = (gemm_m / cfg.gemm_m_per_block) * (gemm_n / cfg.gemm_n_per_block);
        };

        return true;
    };

    return false;
}

bool ConvAsmImplicitGemmGTCDynamicFwdXdlops::IsApplicable(const ConvolutionContext& ctx) const
{
    const auto device_name = ctx.GetStream().GetDeviceName();
    if(!(StartsWith(device_name, "gfx908")))
        return false;

    if(!ctx.use_asm_kernels)
        return false;

    if(!ctx.direction.IsForward())
        return false;

    if(!ctx.Is2d())
        return false;

    if(!ctx.IsFp32())
        return false;

    if(!ctx.rmv.IsV3())
        return false;

    if(ctx.group_counts != 1)
        return false;

    return FindImplicitGemmGtcDynamicFwdKernel(ctx, nullptr, nullptr, nullptr);
}

ConvSolution
ConvAsmImplicitGemmGTCDynamicFwdXdlops::GetSolution(const ConvolutionContext& ctx) const
{
    ConvSolution result;
    KernelInfo kernel;
    std::ostringstream options;

    std::string kernel_name;
    int block_size;
    int grid_size;
    bool ret = FindImplicitGemmGtcDynamicFwdKernel(ctx, &kernel_name, &block_size, &grid_size);
    if(!ret)
        MIOPEN_THROW(
            "This convolution-forward issue could not be solved with igemm dynamic kernels!");

    kernel.kernel_file = "igemm_fwd_gtc_gfx908.s";
    kernel.kernel_name = kernel_name;
    kernel.g_wk.clear();
    kernel.g_wk.push_back(grid_size * block_size);
    kernel.g_wk.push_back(1);
    kernel.g_wk.push_back(1);
    kernel.l_wk.clear();
    kernel.l_wk.push_back(block_size);
    kernel.l_wk.push_back(1);
    kernel.l_wk.push_back(1);

    GenerateClangDefsym(options, "ROCM_METADATA_VERSION", ctx.rmv.UseV3() ? 5 : 4);

    kernel.comp_options = options.str();

    MIOPEN_LOG_I2(kernel.kernel_file + ":" + kernel.kernel_name);

    result.invoker_factory = conv::MakeImplGemmDynamicForwardInvokerFactory(ctx);
    result.construction_params.push_back(kernel);
    return result;
}

